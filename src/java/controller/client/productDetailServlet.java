    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
//        processRequest(request, response);
        int ma_sanpham = Integer.parseInt(request.getParameter("id"));
        ProductDao spd = new ProductDao();
        Product sp = spd.getmaProduct(ma_sanpham);
        request.setAttribute("product", sp);

        List<Product> s = spd.getNewProduct();
        request.setAttribute("spss", s);
        request.getRequestDispatcher("./views/client/home/productDetail.jsp").forward(request, response);
    } 

